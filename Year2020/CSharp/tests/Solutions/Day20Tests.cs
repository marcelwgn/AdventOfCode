                ".#.#.#..##",
                "..#....#..",
                "###...#.#.",
                "..###..###",
                "",
                "Tile 1951:",
                "#.##...##.",
                "#.####...#",
                ".....#..##",
                "#...######",
                ".##.#....#",
                ".###.#####",
                "###.##.##.",
                ".###....#.",
                "..#.#..#.#",
                "#...##.#..",
                "",
                "Tile 1171:",
                "####...##.",
                "#..##.#..#",
                "##.#..#.#.",
                ".###.####.",
                "..###.####",
                ".##....##.",
                ".#...####.",
                "#.##.####.",
                "####..#...",
                ".....##..."
            };

            var tiles = Day20.GetTiles(data);

            var firstTile = new Tile(2311, ".#####..#.", "..##.#..#.", "...#.##..#", "..###..###");
            var secondTile = new Tile(1951, "##.#..#..#", "#.##...##.", ".#####..#.", "#...##.#..");
            var thirdTile = new Tile(1171, "###....##.", "####...##.", ".#..#.....", ".....##...");

            Assert.AreEqual(firstTile, tiles[0]);
            Assert.AreEqual(secondTile, tiles[1]);
            Assert.AreEqual(thirdTile, tiles[2]);
        }

